def tithi_name(tithi_index: int, lang: str = "en") -> str:
    """Full tithi name: 'Shukla Panchami'. tithi_index 0–29."""
    paksha_idx = tithi_index // 15
    within_paksha = tithi_index % 15
    paksha_str = name(PAKSHA, paksha_idx, lang)
    if within_paksha == 14 and paksha_idx == 1:
        tithi_str = AMAVASYA.get(lang, AMAVASYA.get("sa", AMAVASYA["en"]))
    else:
        tithi_str = name(TITHI, within_paksha, lang)
    return f"{paksha_str} {tithi_str}"


