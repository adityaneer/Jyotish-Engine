# ──────────────────────────────────────────────────────────────────
#  Helper — fallback: requested lang → Sanskrit → English
# ──────────────────────────────────────────────────────────────────

def name(data: dict, index: int, lang: str = "en") -> str:
    """
    Get a name from a multi-language dict.
    Fallback order: requested lang → Sanskrit ("sa") → English ("en").

    This means Samvatsara names show in Sanskrit for Telugu/Tamil/etc.
    when no regional translation exists — which matches real panchangas.
    """
    for try_lang in (lang, "sa", "en"):
        if try_lang in data and index < len(data[try_lang]):
            return data[try_lang][index]
    return str(index)


