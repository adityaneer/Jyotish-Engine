def compute_era_years(gregorian_year: int, lang: str = "en") -> list[dict]:
    """
    Compute all era years for a given Gregorian year.
    Note: exact year depends on whether date is before/after Ugadi (Chaitra).
    Returns list of {"label": ..., "year": ...} dicts.
    """
    labels = ERA_LABELS.get(lang, ERA_LABELS.get("sa", ERA_LABELS["en"]))
    results = []
    for era in ERA_SAMVATS:
        year = gregorian_year + era["offset"]
        if year > 0:
            label = labels.get(era["id"], ERA_LABELS["en"].get(era["id"], era["id"]))
            results.append({"id": era["id"], "label": label, "year": year, "epoch": era["epoch"]})
    return results


