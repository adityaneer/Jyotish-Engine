# Python weekday (0=Mon..6=Sun) → formula vaara (1=Ravi..7=Shani)
_PY_TO_VAARA = {6: 1, 0: 2, 1: 3, 2: 4, 3: 5, 4: 6, 5: 7}


def compute_disha_shoola(weekday: int, lang: str = "en") -> dict:
    """
    Disha Shoola for the weekday.
    weekday: Python weekday (0=Mon..6=Sun).
    Returns dict with direction name and key.
    """
    disha_idx = DISHA_SHOOLA_MAP[weekday]
    return {
        "direction": name(DISHA_NAMES, disha_idx, lang),
        "direction_key": ["E", "W", "N", "S"][disha_idx],
    }


def compute_agnivasa(tithi_index: int, weekday: int, lang: str = "en") -> dict:
    """
    Agnivasa — where Agni resides, based on tithi and weekday.
    tithi_index: 0–29 (our system).
    weekday: Python weekday (0=Mon..6=Sun).
    """
    tithi_1_30 = tithi_index + 1
    vaara_num = _PY_TO_VAARA[weekday]
    agni_val = (tithi_1_30 + vaara_num + 1) % 4
    name_idx, is_good = AGNIVASA_RESULT[agni_val]
    return {
        "name": name(AGNIVASA_NAMES, name_idx, lang),
        "guna": name(AGNIVASA_GUNA, name_idx, lang),
        "is_good": is_good,
    }


def compute_shivavasa(tithi_index: int, lang: str = "en") -> dict:
    """
    Shivavasa — where Lord Shiva resides, based on tithi.
    tithi_index: 0–29 (our system).
    Same result for Shukla/Krishna (uses paksha tithi 1–15).
    """
    paksha_tithi = (tithi_index % 15) + 1
    shiva_val = (paksha_tithi * 2 + 5) % 7
    name_idx = SHIVAVASA_RESULT[shiva_val]
    guna = SHIVAVASA_GUNA[name_idx]
    return {
        "name": name(SHIVAVASA_NAMES, name_idx, lang),
        "guna": name(SHIVAVASA_GUNA_LABELS, name_idx, lang),
        "guna_value": guna,
    }
