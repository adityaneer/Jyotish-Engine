def purnimanta_masa_index(amanta_idx: int, paksha_idx: int) -> int:
    """
    Convert Amanta masa to Purnimanta.

    During Shukla paksha: same month name.
    During Krishna paksha: Purnimanta month = Amanta month + 1.
    """
    if paksha_idx == 1:  # Krishna paksha
        return (amanta_idx + 1) % 12
    return amanta_idx


